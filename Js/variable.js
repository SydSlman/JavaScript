// =====JavaScript has three variables type===== //
// 1. VAR 
// 2. LET
// 3. CONST

// VAR = Old tarika variable banane ka, iski value change ho sakti hai.
// LET = Variable banane ka modern tarika, iski value change ho sakti hai.
// CONST = Constant variable, iski value change nahi kar sakte.

//=====Updation with Variable
var name = "Ali"; 
var name = "Ahmed"; 
console.log(name);

//=====Updation in let withhout using let
let age = 20; 
age = 25;
console.log(age);

//=====Do not updation in const 
const pi = 3.14; 
console.log(pi);