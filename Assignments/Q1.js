// 1. Print all the numbers from 0 to 100.
for (var Num = 0; Num <= 100 ; Num++) {
    console.log(Num)
}


// 2. Print all even Numbers from 0 to 100.
for (let Num = 0; Num <= 100; Num++) {
    if (Num % 2 === 0) {
        console.log(Num)
    }
}

// Print all odd Numbers from 0 to 100.
for (let Num = 0; Num <= 100; Num++) {
    if (Num % 2 !== 0) {
        console.log(Num)
    }
}